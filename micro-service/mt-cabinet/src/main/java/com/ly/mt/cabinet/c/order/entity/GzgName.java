package com.ly.mt.cabinet.c.order.entity;

public class GzgName {

    private String id;

    private String name;


    public GzgName() {
    }

    public GzgName(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
